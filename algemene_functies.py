def mijn_functie_1(a):
    return a*a
# Voorbeeld:
print(mijn_functie_1(4))


def mijn_functie_2(a,b):
    uitvoer_lijst = []
    uitvoer_lijst.append(a+b)
    uitvoer_lijst.append(a-b)
    uitvoer_lijst.append(a*b)
    uitvoer_lijst.append(a/b)
    return uitvoer_lijst
# Voorbeeld:
print(mijn_functie_2(12,3))
